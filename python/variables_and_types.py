#!/usr/bin/env python3

# NUMBERS
myint = 7
print(myint)


# To define a floating point number
myfloat = 7.0
print(myfloat)
myfloat = float(7)
print(myfloat)


# STRINGS
mystring = "hello"
print(mystring)
mystring = "hello"
print(mystring)

mystring = "Don't worry about apostrophes"
print(mystring)

# SIMPLE OPERATORS
one = 1
two = 2
three = one + two
print(three)

hello = "hello"
world = "world"
helloworld = hello + " " + world
print(helloworld)

# ASSIGNMENTS
a, b = 3, 4
print(a, b)
